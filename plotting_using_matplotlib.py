#dummy data
subjects = ['English', 'Science', 'Maths', 'Social', 'Hindi']
marks = [78, 92, 88, 74, 81]

# Find Highest Value
highest_mark = max(marks)
highest_subject = subjects[marks.index(highest_mark)]
print("Highest Subject:", highest_subject)
print("Highest Marks:", highest_mark)

# Explode Highest Slice
explode = [0.1 if mark == highest_mark else 0 for mark in marks]
explode

# Custom Colors
colors = ['skyblue', 'orange', 'green', 'pink', 'purple']

#  Change Style
plt.style.use('ggplot')

#  Create Pie Chart
plt.figure(figsize=(5,3))
plt.pie(
    marks,
    labels=subjects,
    colors=colors,
    explode=explode,
    autopct='%1.1f%%',   # show percentage
    shadow=True,
    startangle=90
)

plt.title("Subject Marks Distribution")

# Save Figure
plt.savefig("subject_pie_chart.png")
plt.show()









